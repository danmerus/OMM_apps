import streamlit as st

st.set_page_config(
    page_title="Определение предотвратимости антенатальной смерти плода",
    page_icon="🩺",
    layout="centered",
)

st.title(
    "Способ определения предотвратимости или непредотвратимости "
    "антенатальной смерти плода на сроке доношенной беременности "
    "на основе уровня экспрессии миРНК-126"
)

st.markdown("---")

expression126 = st.number_input(
    "Экспрессия микроРНК 126 в ткани плаценты:",
    min_value=0.0,
    step=0.001,
    format="%.3f",
    placeholder="Введите значение",
)

if st.button("Расчет", use_container_width=True):
    d = -6.941 * expression126 + 6.092

    st.markdown("---")

    if d > 0:
        st.error(
            "Антенатальная смерть плода на сроке доношенной беременности\n\n"
            "**НЕ ПРЕДОТВРАТИМА**"
        )
    else:
        st.success(
            "Антенатальная смерть плода на сроке доношенной беременности\n\n"
            "**ПРЕДОТВРАТИМА**"
        )
